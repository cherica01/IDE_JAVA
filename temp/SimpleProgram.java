public class SimpleProgram {
    public static void main(String[] args) {
        // Déclare deux nombres
        int nombre1 = 10;
        int nombre2 = 20;

        // Effectue l'addition des deux nombres
        int somme = nombre1 + nombre2;

        // Affiche les résultats
        System.out.println("Le premier nombre est : " + nombre1);
        System.out.println("Le deuxième nombre est : " + nombre2);
        System.out.println("La somme des deux nombres est : " + somme);
    }
}
